    private void btnCadastrarEstatisticaJogadorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarEstatisticaJogadorActionPerformed
        int indexJogador = cmbJogador.getSelectedIndex();
        int indexPartida = cmbPartida.getSelectedIndex();
        if(indexJogador >= 0 && indexPartida >= 0){
            ControllerEstatistica controllerEstatistica = new ControllerEstatistica();
            Jogador jogador = this.jogadores.get(indexJogador);
            Partida partida = this.partidas.get(indexPartida);
            Integer pontos = Integer.parseInt(spnPontos.getValue().toString());
            Integer rebotes = Integer.parseInt(spnRebotes.getValue().toString());
            Integer assistencias = Integer.parseInt(spnAssistencias.getValue().toString());
            Integer roubadas = Integer.parseInt(spnRoubadas.getValue().toString());
            Integer tocos = Integer.parseInt(spnTocos.getValue().toString());
            Integer turnovers = Integer.parseInt(spnTurnovers.getValue().toString());

            EstatisticaJogador estatisticaJogador = new EstatisticaJogador(partida, jogador, pontos, rebotes, assistencias, roubadas, tocos, turnovers);

            boolean cadastro = controllerEstatistica.createEstatisticaJogador(estatisticaJogador);
            if(cadastro){
                this.desktop.popUP("Cadastrado com sucesso!");
                this.dispose();
            }else{
                this.desktop.popUP("Erro ao cadastrar estat√≠stica! Por favor verifique os dados inseridos.");
            }
        }else{
            System.out.println(indexJogador);
            System.out.println(indexPartida);
        }
    }//GEN-LAST:event_btnCadastrarEstatisticaJogadorActionPerformed

    private void btnBuscarPartidaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPartidaActionPerformed
        // TODO add your handling code here:
        ControllerPartida cp = new ControllerPartida();
        String data = txtDataPartida.getText();
        this.partidas = new ArrayList();
        this.partidas = cp.getByData(data);
        cmbPartida.removeAllItems();
        for(Partida p : partidas){
            cmbPartida.addItem(p.getEquipe1().getFranquia() + " x " + p.getEquipe2().getFranquia() + " " + p.getData());
        }
        
    }//GEN-LAST:event_btnBuscarPartidaActionPerformed

    private void cmbPartidaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cmbPartidaItemStateChanged
        // TODO add your handling code here:
        cmbJogador.removeAllItems();
        this.jogadores = new ArrayList();
        int index = cmbPartida.getSelectedIndex();
        if(index >= 0){
            Partida partida = this.partidas.get(index);
            Equipe e1 = partida.getEquipe1();
            Equipe e2 = partida.getEquipe2();
            
            ControllerJogador cj = new ControllerJogador();
            ArrayList<Jogador> jogadores = cj.getJogadorByEquipe(e1);
            for(Jogador j : jogadores){
                cmbJogador.addItem(j.getNome());
                this.jogadores.add(j);
            }
            
            jogadores = cj.getJogadorByEquipe(e2);
            System.out.println(e2.getFranquia());
            for(Jogador j : jogadores){
                cmbJogador.addItem(j.getNome());
                this.jogadores.add(j);
            }
        }
        
    }//GEN-LAST:event_cmbPartidaItemStateChanged


