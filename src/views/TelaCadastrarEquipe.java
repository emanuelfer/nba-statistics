    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        String franquia = txtFranquia.getText();
        String cidade = txtCidade.getText();
        String conferencia = cmbConferencia.getSelectedItem().toString();
        
        ControllerEquipe controllerEquipe = new ControllerEquipe();
        boolean cadastro = controllerEquipe.create(franquia, cidade, conferencia);
        
        if(cadastro){
            this.desktop.popUP("Cadastrado com sucesso!");
            this.dispose();
        }else{
            this.desktop.popUP("Erro ao cadastrar equipe! Por favor verifique os dados inseridos.");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed


